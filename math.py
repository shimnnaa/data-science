import math

print("Pi")
print(f"{math.pi}")

print("Euler's Number")
print(f"{math.e}")

print("Square Root")
print(math.sqrt(64))

print("Power Function")
print(math.pow(2, 3))

print("Natural Logarithm")
print(math.log(math.e))

print("Logarithm Base 10")
print(math.log10(100))

print("Ceiling Function")
print(math.ceil(4.2))

print("Floor Function")
print(math.floor(4.8))

print("Absolute Value")
print(math.fabs(-7.5))

print("Factorial")
print(math.factorial(5))

print("Greatest Common Divisor (GCD)")
print(math.gcd(24, 36))

print("Trigonometric Function (Sine)")
angle_degree = 90
angle_radian = math.radians(angle_degree)
print(math.sin(angle_radian))
